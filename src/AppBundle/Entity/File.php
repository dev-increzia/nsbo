    public function base64($base64Content)
    {
        $mimeType = substr($base64Content, 5, strpos($base64Content, ';')-5);

        $base64Content = strstr($base64Content, ';base64,');
        $base64Content = preg_replace('#^;base64,#i', '', $base64Content);
        $data = base64_decode($base64Content);
        $file = tmpfile();

        if ($file === false) {
            throw new \Exception('File can not be opened.');
        }

        /*$f = finfo_open();
        $mimeType = finfo_buffer($f, $data, FILEINFO_MIME_TYPE);*/

        if($this->mime2ext($mimeType)) {
            $extention = '.'.$this->mime2ext($mimeType);
        }else{
            $extention = '.jpg';
        }

        $path = stream_get_meta_data($file)['uri'];
        file_put_contents($path . $extention, $data);
        $uploadedFile = new UploadedFile($path . $extention, $path . $extention, $mimeType, null, null, true);
        $this->setFile($uploadedFile);
    }

    function getMIMETYPE($base64string){
        preg_match("#^data:(.*);base64,#i",$base64string, $match);
        return $match[1];
    }



